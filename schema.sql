-- ------------------------------------------------------------
-- secciones
-- ------------------------------------------------------------
CREATE TABLE IF NOT EXISTS secciones (
  id       SERIAL     PRIMARY KEY,
  grado_id INT        NOT NULL,
  nombre   VARCHAR(5) NOT NULL,
